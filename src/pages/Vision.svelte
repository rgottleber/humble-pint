  <div class="text-gray-600">
    At Humble Pint Brewing, we want to brew and serve great craft beer in a
    welcoming, friendly taproom environment to our superb guests. Those aims
    should be at the top of any small independent brewery’s list of goals, as
    they are for us, but we’d like to go a bit further.
  </div>
  <div
    class="text-center my-6 mx-24 py-4 text-gray-400 md:text-xl lg:text-3xl font-black justify-center border-t-2 border-b-2 border-gray-400">
    …For Our Community
  </div>

  <div class="text-gray-600">
    We want to create a place for people to meet and talk about their day, enjoy
    each other’s company, play a game or two or just give their dog a rest
    mid-walk. In short, we want to be a part of the community, and that starts
    with being good neighbors – both inside our taproom, with our customers, and
    outside it by getting involved with other local businesses, city leaders and
    charitable organizations.
  </div>

  <div
    class="text-center my-6 mx-24 py-4 text-gray-400 md:text-xl lg:text-3xl font-black justify-center border-t-2 border-b-2 border-gray-400">
    …For Our Environment
  </div>

  <div class="text-gray-600">
    Humble Pint Brewing takes environmental concerns seriously. We will
    constantly work to improve our operation’s impact through recycled/upcycled
    use of brewing grain, methods for reduced water usage during equipment
    cleaning, investigation of solar options for powering the brew house and
    taproom, and analysis of possible alternate uses for other brewing
    byproducts, such as side-streaming brewery waste water.
  </div>

  <div class="font-black text-4xl pt-4 pb-2">Our Story</div>

  <div class="text-gray-600">
    We’re Jared and Alicia Wennstrom – owners and operators of Humble Pint
    Brewing! When we moved to Central Texas from our native state of Oregon
    twenty years ago, we thought the move would be temporary, but we quickly
    discovered a love for the Austin area and after a time the new friends we’d
    made became family. The years passed and “you guys” turned into “y’all”‘s
    and our summers picking blackberries on the Pacific Coast turned into
    summers tubing on the Guadalupe River. Our children grew up on chips and
    queso and spent hours with glue guns creating three foot long ‘mums for
    Homecoming.
  </div>

  <div class="text-gray-600">
    Texas is a great place to raise kids but we still thought we’d eventually
    settle in the Pacific Northwest when all was said and done. After our second
    Texas-born grandchild arrived, however, we realized that Texas was our home
    and there was no going back! What’s a pair of Oregonians to do when they
    miss the land of their birth but can’t bear to leave the place and people
    they’ve grown to love? Open a PNW style brewery so they can share the
    wonders of the Pacific Northwest with their home away from home, of course!
  </div>

  <div class="text-gray-600">
    Humble Pint Brewing Co. is a brewpub that welcomes all beer lovers to its
    Leander location! They offer a number of their own delicious craft beers as
    well as house-made artisan pizzas and salads.
  </div>

  <div class="text-gray-600">
    The combination of great craft beer, Texas-friendly service and the relaxed
    and welcoming atmosphere of an Oregon-style brewery is sure to be
    irresistible!
  </div>
